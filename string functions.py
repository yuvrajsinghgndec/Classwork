mystr = "My name is vicky"
print(mystr)
print(len(mystr))
# string slicing
print(mystr[0:5])
print(mystr[4])
print(mystr[0:])
print(mystr[:5])
print(mystr[0:16:1])
print(mystr[0:16:2])
# to display string in reverse direction
print(mystr[::-1])
print(mystr[::-2])
# string functions
print(mystr.count("m"))
print(mystr.upper())
print(mystr.lower())
print(mystr.find("name"))
print(mystr.endswith("is"))
print(mystr.replace("vicky", " ritesh "))
