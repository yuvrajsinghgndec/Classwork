#  arithmetic operators
print("arithmetic operators")
a = 5
b = 2
print(id(a))
print(a + b)
print(a - b)
print(a / b)
print(a % b)
print(a // b)
print(a ** b)

# relational operators
print("relational operators")
print(a > b)
print(a < b)
print(a == b)
print(a != b)
print(a <= b)

# logical operators
print("logical operators")
c = 15
d = 4
e = 20
print(c > d and e < c)
print(c > d or e < c)
print(not(c > d))

# bitwise operators
print("bitwise operators")
print(c & d)
print(c | d)
print(c ^ d)
print(~d)

